/**
 * позиция метки относительно элемента управления
 */
export enum TLabelPos {
    /**
     * отображать метку выше управляющего элемента
     */
    lbpAbove = 0,
        /**
         * отображать метку ниже управляющего элемента
         */
    lbpBelow = 1,
        /**
         * отображать метку слева от управляющего элемента
         */
    lbpLeft = 2,
        /**
         * отображать метку справа от управляющего элемента
         */
    lbpRight = 3
}