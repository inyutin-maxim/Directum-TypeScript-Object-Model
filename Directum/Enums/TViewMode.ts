/**
 * Режим отображения представления
 */
export enum TViewMode {
    /**
     *  Представление отображается в режиме «Просмотр»
     */
    vmView = 0,
        /**
         * Представление отображается в режиме «Выбор»
         */
    vmSelect = 1,
        /**
         * Представление отображается в режиме «Навигация»
         */
    vmNavigation = 2
}